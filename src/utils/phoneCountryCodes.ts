export function combineDialAndNational(dial: string, national: string): string {
  const d = String(dial || '').replace(/\D/g, '');
  let n = String(national || '').replace(/\D/g, '');
  if (n.startsWith('0')) n = n.slice(1);
  if (!d || !n) return '';
  return d + n;
}
